package Main;

public class DataKaryawan {
	
	String kodeKaryawan = "";
	String namaKaryawan = "";
	String jenisKelamin = "";
	String jabatan = "";
	int gajiKaryawan = 0;
	
	
	
	public DataKaryawan(String kodeKaryawan, String namaKaryawan, String jenisKelamin, String jabatan,
			int gajiKaryawan) {
		super();
		this.kodeKaryawan = kodeKaryawan;
		this.namaKaryawan = namaKaryawan;
		this.jenisKelamin = jenisKelamin;
		this.jabatan = jabatan;
		this.gajiKaryawan = gajiKaryawan;
	}

}
